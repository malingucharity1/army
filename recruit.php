<h1 align="center" style="color:#0000FF">THE UPDF SHALL CONDUCT RECRUITMENT  STARTING 05th SEPTEMBER 2014</h1>
<p><marquee><i><h1 align="center" style="color:#FF0000">ABOUT THE RECRUITMENT</h1></i></marquee></p>
<p>The Uganda People Forces will recruit 5000 personnel from all the districts of Uganda, on quota basis. Both recruit infantry and specialised unit soldiers and proffessionals will be recruited, within their respective district quotas, if they meet the recruitment criteria.</p>
<p><h1 align="center" style="color:#CC0099">UPDF RECRUITMENT CRITERIA</h1></p>
<p>
  <p><b>1.Citizenship.</b></p>
  <p>All recruits MUST be citizens of Uganda. Uganda National Identity Cards will be mandatory</p>
  <p><b>2.Medical.</b></p>
  <p>All recruits MUST be healthy medically physically fit, ready to undergo medical and physical body check and a road run to test endurance and body fitness. Testing MUST be done on senses of hearing,sight,touch,taste,smell,flat foot and general heath.Psychiatric and pregnancy tests will also be carried out.</p>
  <p><b>3.Age.</b></p>
  <p>All recruits MUST be adult males and females between the ages of (18-25) years.Marital status MUST be single with no children.</p>
  <p><b>4.Educational Standards.</b></p>
  <p>All recruits of infantary  units MUST posses a minimum formal education of <b>S.4(UCE),</b> or its equivalent, and <b>S.6(UACE)</b> with bias in sciences or its equivalent for specialised units. Original Certificates of the passslips shall be mandatory.<b>professional graduates</b> will also be recruited provided they will be ready to undergo basic military training. Age limit for professionals is 18-30 years.</p> 
  <p><b>5.Conduct.</b></p>
  <p>Alltherecruits must be of good conduct with no criminal record. Letters of recommendations from LC I, LC III, GISOstheir duly signed are a MUST to confirm thei conduct.</p>
  <p><b>6.</b> Any presentation of forged documents is criminal and culprits will be liable for prosection.</p>
